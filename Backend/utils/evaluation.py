from sklearn.metrics import mean_absolute_error, mean_squared_error, mean_absolute_percentage_error, root_mean_squared_error

def evaluate_regression(y_true, y_pred):
    """
        Compute multiple regression evaluation metrics for the comparison of true and predicted
        values in a regression model. Useful for statistical analysis by returning a dictionary
        of commonly used error metrics, including mean squared error (MSE), root mean squared
        error (RMSE), mean absolute error (MAE), and mean absolute percentage error (MAPE).

        Parameters:
        y_true: Array-like
            The true values observed in the dataset.

        y_pred: Array-like
            The predicted values generated by the regression model.

        Returns:
        dict
            A dictionary containing the calculated metrics:
            MSE (float): The mean squared error between y_true and y_pred.
            RMSE (float): The root mean squared error between y_true and y_pred.
            MAE (float): The mean absolute error between y_true and y_pred.
            MAPE (float): The mean absolute percentage error between y_true and y_pred.
    """
    mse = mean_squared_error(y_true, y_pred)
    rmse = root_mean_squared_error(y_true, y_pred)
    mae = mean_absolute_error(y_true, y_pred)
    mape = mean_absolute_percentage_error(y_true, y_pred)
    return {
        "MSE": mse,
        "RMSE": rmse,
        "MAE": mae,
        "MAPE": mape
    }